import UIKit
import SwiftUI

extension UIColor {
    
    @nonobjc class var lightTeal: UIColor {
        return UIColor(named: "LightTeal")!
    }
    
    @nonobjc class var paleYellow: UIColor {
        return UIColor(named: "PaleYellow")!
    }
    
    @nonobjc class var royalBlue: UIColor {
        return UIColor(named: "RoyalBlue")!
    }
    
    @nonobjc class var middleGreen: UIColor {
        return UIColor(named: "MiddleGreen")!
    }
    
    @nonobjc class var tertiary: UIColor {
        return UIColor(named: "Tertiary")!
    }
    
    @nonobjc class var lightFantasyGreen: UIColor {
        return UIColor(named: "LightFantasyGreen")!
    }
    
    @nonobjc class var defaultPurple: UIColor {
        return UIColor(named: "DefaultPurple")!
    }
    
    @nonobjc class var lightBackgroundGray: UIColor {
        return UIColor(named: "LightBackgroundGray")!
    }
    
    @nonobjc class var lightSecondary: UIColor {
        return UIColor(named: "LightSecondary")!
    }
    
    @nonobjc class var darkFansyGreen: UIColor {
        return UIColor(named: "DarkFansyGreen")!
    }
}

extension Color {
    @nonobjc static var lightTeal: Color {
        return Color("LightTeal")
    }
    
    @nonobjc static var paleYellow: Color {
        return Color("PaleYellow")
    }
    
    @nonobjc static var royalBlue: Color {
        return Color("RoyalBlue")
    }
    
    @nonobjc static var middleGreen: Color {
        return Color("MiddleGreen")
    }
    
    @nonobjc static var tertiary: Color {
        return Color("Tertiary")
    }
    
    @nonobjc static var lightFantasyGreen: Color {
        return Color("LightFantasyGreen")
    }
    
    @nonobjc static var defaultPurple: Color {
        return Color("DefaultPurple")
    }
    
    @nonobjc static var lightBackgroundGray: Color {
        return Color("LightBackgroundGray")
    }
    
    @nonobjc static var lightSecondary: Color {
        return Color("LightSecondary")
    }
    
    @nonobjc static var darkFansyGreen: Color {
        return Color("DarkFansyGreen")
    }
}
