import UIKit
import SwiftUI

extension UIColor {
    
    @nonobjc static let lightTeal: UIColor = UIColor(named: "LightTeal")!
    @nonobjc static let paleYellow: UIColor = UIColor(named: "PaleYellow")!
    @nonobjc static let royalBlue: UIColor = UIColor(named: "RoyalBlue")!
    @nonobjc static let middleGreen: UIColor = UIColor(named: "MiddleGreen")!
    @nonobjc static let tertiary: UIColor = UIColor(named: "Tertiary")!
    @nonobjc static let lightFantasyGreen: UIColor = UIColor(named: "LightFantasyGreen")!
    @nonobjc static let defaultPurple: UIColor = UIColor(named: "DefaultPurple")!
    @nonobjc static let lightBackgroundGray: UIColor = UIColor(named: "LightBackgroundGray")!
    @nonobjc static let lightSecondary: UIColor = UIColor(named: "LightSecondary")!
    @nonobjc static let darkFansyGreen: UIColor = UIColor(named: "DarkFansyGreen")!
    
}

extension Color {
    
    @nonobjc static let lightTeal: Color = Color(uiColor: .lightTeal)
    @nonobjc static let paleYellow: Color = Color(uiColor: .paleYellow)
    @nonobjc static let royalBlue: Color = Color(uiColor: .royalBlue)
    @nonobjc static let middleGreen: Color = Color(uiColor: .middleGreen)
    @nonobjc static let tertiary: Color = Color(uiColor: .tertiary)
    @nonobjc static let lightFantasyGreen: Color = Color(uiColor: .lightFantasyGreen)
    @nonobjc static let defaultPurple: Color = Color(uiColor: .defaultPurple)
    @nonobjc static let lightBackgroundGray: Color = Color(uiColor: .lightBackgroundGray)
    @nonobjc static let lightSecondary: Color = Color(uiColor: .lightSecondary)
    @nonobjc static let darkFansyGreen: Color = Color(uiColor: .darkFansyGreen)
    
}
